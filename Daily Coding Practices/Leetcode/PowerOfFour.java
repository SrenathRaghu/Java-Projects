package com.java;

public class PowerOfFour {
	
	public static void main(String[] args) {
		
		
	int n = 20;
	if(n<1)
        System.out.println("false"); 
	
    while(n%4==0){
        n=n/4;
    }
   
    
    if(n==1)
    {
    System.out.println("true");
    }
    
    else {
    	System.out.println("false"); 
    	
    }
    		
	}
	
		
	}


